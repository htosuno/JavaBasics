public class Patterns {

	public static void main(String[] args) {
		for(int i = 1; i < 5; i++) {
			for(int j = 1; j < 6; j++)System.out.print('*');
			System.out.println();
		}
		
		for(int i = 1; i < 11; i++) {
			for(int j = 1; j < 11; j++)System.out.print('*');
			System.out.println();
		}
	}

}
